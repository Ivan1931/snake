import java.util.LinkedList;

public class Snake {
	private int snakeNumber;
	private LinkedList<Integer[]> coordinates = new LinkedList<Integer[]>();
	
	public Snake(int SnakeNumber, Integer[] Coordinates)
	{
		snakeNumber = SnakeNumber;
		addCoordinates(Coordinates);
	}
	public int getSnakeNumber() {
		return snakeNumber;
	}
	public void setSnakeNumber(int snakeNumber) {
		this.snakeNumber = snakeNumber;
	}
	public LinkedList<Integer[]> getCoordinates() {
		return coordinates;
	}
	public void setCoordinates(LinkedList<Integer[]> coordinates) {
		this.coordinates = coordinates;
	}
	public void addCoordinates (Integer[] Coordinates)
	{
		this.coordinates.add(Coordinates);
	}
	
}
